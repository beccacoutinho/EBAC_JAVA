package TarefaCalculoMedia;

public class Exercicio {
    public static void main(String args[]) {
        int nota1 = 7;
        int nota2 = 8;
        int nota3 = 9;
        int nota4 = 5;

        int media = (nota1 + nota2 + nota3 + nota4) /4;

        System.out.println("O aluno tirou as seguintes notas nas provas: 7, 8, 9, 5. ");
        System.out.println("Ele ficou com a nota " + media + " como media.");
    }
}
